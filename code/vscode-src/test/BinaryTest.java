package test;
public class BinaryTest {
    public static void main(String[] args) {
        // binary
        int n1 = 0b1010;
        // decimal
        int n2 = 1010;
        // octonary
        int n3 = 01010;
        // hexadecimal
        int n4 = 0x10101;

        System.out.println(n1);

        System.out.println(n2);

        System.out.println(n3);

        System.out.println(n4);
    }
}